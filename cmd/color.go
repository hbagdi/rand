package cmd

import (
	"errors"
	"fmt"
	"strconv"

	"github.com/brianvoe/gofakeit"
	"github.com/spf13/cobra"
)

var rgb bool
var hex bool

// colorCmd represents the color command
var colorCmd = &cobra.Command{
	Use:   "color",
	Short: "Generate a random color",
	Long: `Generate a random color
Default colors will be english names of colors and shades.
Hex and RGB formatted colors can be generated by using the flags.
Hex format is "#4285f4".
RGB format is "rgb(66,133,244)".
	`,
	RunE: func(cmd *cobra.Command, args []string) error {
		if hex && rgb {
			return errors.New("--hex and --rgb are exclusive")

		}
		for i := 0; i < count; i++ {
			if hex {
				fmt.Println(gofakeit.HexColor())
			} else if rgb {
				rgb := gofakeit.RGBColor()
				fmt.Println("rgb(" + strconv.Itoa(rgb[0]) + "," +
					strconv.Itoa(rgb[1]) + "," + strconv.Itoa(rgb[2]) + ")")
			} else {
				fmt.Println(gofakeit.Color())
			}
		}
		return nil
	},
}

func init() {
	colorCmd.Flags().BoolVar(&hex, "hex", false, "output a color in hex format '#4285f4'")
	colorCmd.Flags().BoolVar(&rgb, "rgb", false, "output a color in rgb format 'rgb(66,133,244)'")
	rootCmd.AddCommand(colorCmd)
}
